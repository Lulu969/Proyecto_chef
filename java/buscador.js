function buscar() {
    const nombre = document.getElementById('nombre').value.trim().toLowerCase();

    if (nombre === '') {
        alert('Ingrese el nombre de una receta.');
        return;
    }

    const regex = /^[a-zA-Z0-9\sáéíóúÁÉÍÓÚñÑ]+$/;
    if (!regex.test(nombre)) {
        alert("Por favor, introduce un nombre de receta válido que contenga solo letras, números y espacios.");
        return;
    }

    let recetaEncontrada;
    for (let i = 0; i < discografia.length; i++) {
        if (discografia[i].nombre.toLowerCase() === nombre) {
            recetaEncontrada = discografia[i];
            break;
        }
    }

    if (recetaEncontrada) {
        document.getElementById('title').innerText = "Receta de " + recetaEncontrada.nombre

        const listaIngredientes = document.getElementById('ingredientes');
        listaIngredientes.innerHTML = '';
        recetaEncontrada.ingredientes.forEach(ingrediente => {
            const li = document.createElement('li');
            li.textContent = ingrediente;
            listaIngredientes.appendChild(li);
        });

        document.getElementById('preparacion').innerText = recetaEncontrada.preparacion;
        document.getElementById('personas').innerText = "Rinde para " + recetaEncontrada.personas + " personas.";
    } else {
        alert("La receta no se encuentra en la lista.");
    }
}